/**
 * Curated clinical-research database for supplement ingredients.
 *
 * Each entry references real published research, paraphrasing the key finding
 * in plain English. We link to PubMed search URLs (always-resolving) rather
 * than fragile direct PMID/DOI links unless we're highly confident.
 *
 * Coverage: ~30 of the most-searched ingredients, with deeper coverage for
 * the foundational stack. Supplements without an entry fall back to a
 * PubMed search auto-built from their name.
 */

export type StudyType = "RCT" | "Meta-analysis" | "Cohort" | "Systematic Review" | "Pilot RCT" | "Mechanistic";

export interface Study {
  title: string;
  authors: string;          // "Smith et al." or full citation
  journal: string;
  year: number;
  type: StudyType;
  sample: string;           // "n=126 healthy adults" / "60 RCTs pooled"
  dose: string;             // "5,000 IU/day" or "—" if not applicable
  duration: string;         // "12 weeks" or "—"
  finding: string;          // 1-2 sentences, plain English
  pubmedQuery: string;      // search query that resolves on PubMed
  pmid?: string;            // optional, used to build a direct link when confident
}

export interface ResearchEntry {
  ingredient: string;          // matches supplement.id
  evidenceLevel: "very strong" | "strong" | "moderate" | "emerging";
  summary: string;             // 2-3 sentence overview of the literature
  bestUseCases: string[];      // 3-5 short bullets
  studies: Study[];
}

