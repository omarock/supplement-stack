// ─── Helpers ──────────────────────────────────────────────────────────────

export function getResearch(slug: string): ResearchEntry | undefined {
  return RESEARCH[slug];
}

export function getStudyCount(slug: string): number {
  return RESEARCH[slug]?.studies.length ?? 0;
}

export function buildPubmedSearchUrl(query: string): string {
  return `https://pubmed.ncbi.nlm.nih.gov/?term=${query}`;
}

export function buildStudyLink(study: Study): string {
  if (study.pmid) return `https://pubmed.ncbi.nlm.nih.gov/${study.pmid}/`;
  return buildPubmedSearchUrl(study.pubmedQuery);
}

/**
 * Fallback for ingredients without a curated entry — points to a useful
 * PubMed search built from the supplement's name and key tags.
 */
export function fallbackResearchUrl(supplementName: string, tags: string[] = []): string {
  const q = [supplementName, ...tags.slice(0, 3)].join(" ");
  return buildPubmedSearchUrl(pm(q));
}
